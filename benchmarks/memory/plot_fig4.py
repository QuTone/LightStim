"""
Regenerate fig4_se_scheduling.png — SE scheduling comparison (Z basis).

Reads: eval/memory_benchmark/results/se_scheduling.csv
       (generated by run_se_scheduling.py)

Shows LER vs physical error rate p for two scheduling strategies
(perpendicular = FT, swapped = non-FT) at d=3,5,7,9 in Z basis.

Legend layout: two boxes — "Scheduling" (linestyle) + "Distance" (color).

Usage:
    venv/bin/python -m eval.memory_benchmark.plot_fig4
"""

import pandas as pd
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.lines as mlines
from pathlib import Path

from lightstim.plot.styles import apply_paper_style, bold_ticks, PALETTE_DISTANCE

RESULTS = Path(__file__).resolve().parent / "results"
CSV     = RESULTS / "se_scheduling.csv"
OUTPUT  = RESULTS / "fig4_se_scheduling.png"

DISTANCES  = [3, 5, 7, 9]
BASIS      = "Z"

SCHED_STYLE = {
    "perpendicular": dict(ls="-",  marker="o", label="FT (Standard)"),
    "swapped":       dict(ls="--", marker="s", label="non-FT (hook)"),
}


def plot(df, save_path):
    df_z = df[df["basis"] == BASIS].copy()

    apply_paper_style()
    fig, ax = plt.subplots(figsize=(3.8, 4.2))

    for sched, style in SCHED_STYLE.items():
        df_s = df_z[df_z["scheduling"] == sched]
        for d in DISTANCES:
            df_d = df_s[df_s["distance"] == d].sort_values("p")
            if df_d.empty:
                continue
            color = PALETTE_DISTANCE.get(d, "gray")
            ax.loglog(
                df_d["p"], df_d["logical_error_rate"],
                marker=style["marker"], color=color,
                linestyle=style["ls"], markeredgecolor="none",
            )

    ax.set_xlabel("Physical Error Rate $p$")
    ax.set_ylabel("LER")
    ax.set_title("SE Scheduling", pad=10)
    ax.grid(True, which="both", ls="--", linewidth=0.5, alpha=0.5)
    bold_ticks(ax)

    # ── Two-box legend ────────────────────────────────────────────────
    # Box 1: scheduling line style
    sched_handles = [
        mlines.Line2D([], [], color="black", ls=style["ls"],
                      marker=style["marker"], markersize=6,
                      markeredgecolor="none", linewidth=1.8,
                      label=style["label"])
        for style in SCHED_STYLE.values()
    ]
    # Box 2: distance color
    dist_handles = [
        mlines.Line2D([], [], color=PALETTE_DISTANCE.get(d, "gray"),
                      ls="none", marker="o", markersize=7,
                      markeredgecolor="none", label=f"d={d}")
        for d in DISTANCES if d in [r for r in df_z["distance"].unique()]
    ]

    # Draw Distance legend at lower right first, then place Scheduling to its left
    leg2 = ax.legend(handles=dist_handles, title="Distance",
                     loc="lower right", frameon=True, framealpha=0.7)
    leg2.get_title().set_fontweight("bold")
    ax.add_artist(leg2)

    fig.canvas.draw()
    bb2 = leg2.get_window_extent(fig.canvas.get_renderer())
    bb2_ax = bb2.transformed(ax.transAxes.inverted())
    gap = 0.02  # axes fraction gap between the two boxes

    leg1 = ax.legend(handles=sched_handles, title="Scheduling",
                     loc="lower right", frameon=True, framealpha=0.7,
                     bbox_to_anchor=(bb2_ax.x0 - gap, bb2_ax.y0),
                     bbox_transform=ax.transAxes)
    leg1.get_title().set_fontweight("bold")

    fig.tight_layout()
    fig.savefig(save_path, dpi=180, bbox_inches="tight")
    print(f"Saved: {save_path}")
    plt.close(fig)


if __name__ == "__main__":
    if not CSV.exists():
        raise FileNotFoundError(
            f"Data not found: {CSV}\n"
            "Run: venv/bin/python eval/memory_benchmark/run_se_scheduling.py"
        )
    df = pd.read_csv(CSV)
    print(f"Loaded {len(df)} rows — "
          f"schedulings: {sorted(df['scheduling'].unique())}, "
          f"distances: {sorted(df['distance'].unique())}")
    plot(df, OUTPUT)
