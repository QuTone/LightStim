# ─────────────────────────────────────────────────────────────────────────────
# Helper: build a noiseless Z-memory circuit for a given BB code
# ─────────────────────────────────────────────────────────────────────────────

def build_bb_circuit(l, m, rounds=6):
    """Build noiseless BB code circuit; return (circuit, patch, system)."""
    A = [[3, 0], [0, 1], [0, 2]]
    B = [[0, 3], [1, 0], [2, 0]]
    patch = BBCode(l=l, m=m, A=A, B=B)
    system = QECSystem()
    system.add_patch(patch)
    noise_cfg = NoiseConfig(p_1q=0, p_2q=0, p_meas=0, p_reset=0)
    exp = MemoryExperiment(
        system, BBCodeExtractionBlock,
        rounds=rounds,
        noise_params=noise_cfg,
        noise_model='circuit_level',
        basis='Z',
    )
    circuit = exp.build()
    return circuit, patch, system


# ─────────────────────────────────────────────────────────────────────────────
# Helper: identify stabilizer type from ancilla coordinate
# ─────────────────────────────────────────────────────────────────────────────

def get_stabilizer_type_from_coord(x, y):
    """
    BB code layout:
      Even rows (y=2i):    X-ancilla at (2j, y) — even x, even y
      Odd rows (y=2i+1):   Z-ancilla at (2j+1, y) — odd x, odd y
    Returns 'X', 'Z', or 'unknown'.
    """
    xi, yi = int(round(x)), int(round(y))
    if yi % 2 == 0 and xi % 2 == 0:
        return 'X'
    elif yi % 2 == 1 and xi % 2 == 1:
        return 'Z'
    else:
        return 'unknown'


# ─────────────────────────────────────────────────────────────────────────────
# Core analysis: parse flattened circuit for DETECTOR instructions
# ─────────────────────────────────────────────────────────────────────────────

def analyze_detectors(circuit: stim.Circuit, label: str, patch, rounds: int):
    print(f"\n{'='*70}")
    print(f"Analysis: {label}")
    print(f"{'='*70}")

    # Code parameters
    n_data   = len(patch.data_indices)
    n_x_stab = len(patch.syndrome_indices_x)
    n_z_stab = len(patch.syndrome_indices_z)
    k        = patch.num_logicals
    print(f"\nCode parameters:")
    print(f"  n (data qubits)     = {n_data}")
    print(f"  n_X stabilizers     = {n_x_stab}")
    print(f"  n_Z stabilizers     = {n_z_stab}")
    print(f"  k (logical qubits)  = {k}")
    print(f"  rounds              = {rounds}")

    # ── Flatten the circuit ──────────────────────────────────────────────────
    flat = circuit.flattened()

    # Count total DETECTOR instructions
    total_detectors = sum(1 for inst in flat if inst.name == 'DETECTOR')
    print(f"\nTotal DETECTOR instructions (flattened): {total_detectors}")

    # ── Parse each DETECTOR and extract its time coordinate ──────────────────
    # DETECTOR annotations: stim uses coordinate tuples; for BB code they look
    # like (x, y, t) where t is the time/round index.
    # We collect: time_coord -> list of (x, y) spatial coords for classification.

    detectors_by_time = collections.defaultdict(list)
    detectors_no_coord = 0
    all_times = set()

    for inst in flat:
        if inst.name != 'DETECTOR':
            continue
        coords = inst.gate_args_copy()  # list of floats: [x, y, t, ...]
        if len(coords) >= 3:
            t = coords[2]
            x, y = coords[0], coords[1]
            all_times.add(t)
            detectors_by_time[t].append((x, y))
        elif len(coords) == 2:
            # 2D coords only — no time
            x, y = coords[0], coords[1]
            detectors_by_time['no_time'].append((x, y))
            detectors_no_coord += 1
        else:
            detectors_no_coord += 1

    times_sorted = sorted(t for t in all_times if isinstance(t, (int, float)))

    print(f"\nTime coordinates found: {times_sorted}")
    print(f"Detectors with no/partial coords: {detectors_no_coord}")

    # ── Per-round breakdown ──────────────────────────────────────────────────
    print(f"\nPer time-slice breakdown:")
    print(f"  {'time':>6}  {'count':>6}  {'X-type':>8}  {'Z-type':>8}  {'unknown':>8}")
    print(f"  {'-'*6}  {'-'*6}  {'-'*8}  {'-'*8}  {'-'*8}")

    total_x = 0
    total_z = 0
    total_unk = 0
    counts_per_time = {}

    for t in times_sorted:
        dlist = detectors_by_time[t]
        n_x = sum(1 for (x, y) in dlist if get_stabilizer_type_from_coord(x, y) == 'X')
        n_z = sum(1 for (x, y) in dlist if get_stabilizer_type_from_coord(x, y) == 'Z')
        n_u = sum(1 for (x, y) in dlist if get_stabilizer_type_from_coord(x, y) == 'unknown')
        total_x += n_x
        total_z += n_z
        total_unk += n_u
        counts_per_time[t] = {'total': len(dlist), 'X': n_x, 'Z': n_z, 'unknown': n_u}
        print(f"  {t:>6.1f}  {len(dlist):>6}  {n_x:>8}  {n_z:>8}  {n_u:>8}")

    if 'no_time' in detectors_by_time:
        dlist = detectors_by_time['no_time']
        n_x = sum(1 for (x, y) in dlist if get_stabilizer_type_from_coord(x, y) == 'X')
        n_z = sum(1 for (x, y) in dlist if get_stabilizer_type_from_coord(x, y) == 'Z')
        print(f"  {'no_t':>6}  {len(dlist):>6}  {n_x:>8}  {n_z:>8}")

    print(f"\nGrand totals by type:")
    print(f"  X-type detectors: {total_x}")
    print(f"  Z-type detectors: {total_z}")
    print(f"  unknown:          {total_unk}")
    print(f"  TOTAL:            {total_x + total_z + total_unk + detectors_no_coord}")

    # ── Round categorization ──────────────────────────────────────────────────
    # Stim time coordinates: t=1 = first SE round, t=rounds = last SE round
    # Final measurement round has t=rounds+0.5 or t=rounds+1 (or no detectors at a separate time)
    # Let's categorize:
    #   first SE round: min(times)
    #   last measurement: max(times)  [if different from SE rounds]
    #   middle SE rounds: everything in between

    if times_sorted:
        t_first = times_sorted[0]
        t_last  = times_sorted[-1]
        t_middle = times_sorted[1:-1]

        count_first  = counts_per_time[t_first]['total']
        count_last   = counts_per_time[t_last]['total']
        count_middle = sum(counts_per_time[t]['total'] for t in t_middle)
        n_middle_rounds = len(t_middle)

        print(f"\nRound-category breakdown:")
        print(f"  First SE round  (t={t_first}):  {count_first} detectors")
        if n_middle_rounds > 0:
            per_middle = count_middle / n_middle_rounds if n_middle_rounds else 0
            print(f"  Middle SE rounds (t={t_middle[0]}..{t_middle[-1]}):  "
                  f"{count_middle} detectors across {n_middle_rounds} rounds "
                  f"= {per_middle:.1f}/round")
        print(f"  Final round     (t={t_last}):  {count_last} detectors")
        print(f"  TOTAL: {count_first + count_middle + count_last}")

    # ── X/Z per round detail ─────────────────────────────────────────────────
    print(f"\nX vs Z detail per time slice:")
    for t in times_sorted:
        c = counts_per_time[t]
        print(f"  t={t:.1f}: {c['total']:3d} total  ({c['X']:3d} X-type, {c['Z']:3d} Z-type, {c['unknown']:3d} unknown)")

    # ── Hypothesis test ──────────────────────────────────────────────────────
    print(f"\nHypothesis verification:")
    print(f"  n_X stabilizers = {n_x_stab}  (independent generators span {n_x_stab - k//2} dims)")
    print(f"  n_Z stabilizers = {n_z_stab}  (independent generators span {n_z_stab - k//2} dims)")
    n_indep_x = n_x_stab - k // 2
    n_indep_z = n_z_stab - k // 2
    print(f"  Independent X-checks: {n_indep_x}")
    print(f"  Independent Z-checks: {n_indep_z}")

    gongaa_formula = n_z_stab * (rounds + 1)
    lightstim_total = total_detectors
    print(f"\n  gongaa Z-only formula: n_Z × (rounds+1) = {n_z_stab} × {rounds+1} = {gongaa_formula}")
    print(f"  LightStim total detectors: {lightstim_total}")
    print(f"  Excess over gongaa: {lightstim_total - gongaa_formula}")
    print(f"    (excess = X-type detectors = {total_x})")

    # ── Show the raw circuit summary (non-flattened) for structure ───────────
    print(f"\nRaw circuit instruction count (non-flattened): {len(list(circuit))}")
    detector_instrs_raw = [inst for inst in circuit if inst.name == 'DETECTOR']
    repeat_instrs_raw   = [inst for inst in circuit if inst.name == 'REPEAT']
    print(f"  Top-level DETECTOR instructions: {len(detector_instrs_raw)}")
    print(f"  Top-level REPEAT  instructions:  {len(repeat_instrs_raw)}")

    return counts_per_time, total_detectors, total_x, total_z


# ─────────────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────────────

def main():
    ROUNDS = 6

    # ── [[72,12,6]] ─────────────────────────────────────────────────────────
    print("\nBuilding BB [[72,12,6]] (l=6, m=6)...")
    circuit_72, patch_72, system_72 = build_bb_circuit(l=6, m=6, rounds=ROUNDS)
    counts_72, total_72, x_72, z_72 = analyze_detectors(
        circuit_72, "BB [[72,12,6]] — 6 rounds, Z-memory", patch_72, ROUNDS
    )

    # ── [[144,12,12]] ───────────────────────────────────────────────────────
    print("\n\nBuilding BB [[144,12,12]] (l=12, m=6)...")
    circuit_144, patch_144, system_144 = build_bb_circuit(l=12, m=6, rounds=ROUNDS)
    counts_144, total_144, x_144, z_144 = analyze_detectors(
        circuit_144, "BB [[144,12,12]] — 6 rounds, Z-memory", patch_144, ROUNDS
    )

    # ── Cross-code summary ──────────────────────────────────────────────────
    print(f"\n{'='*70}")
    print("SUMMARY COMPARISON")
    print(f"{'='*70}")
    print(f"\n{'Code':<20} {'n':>5} {'n_X':>5} {'n_Z':>5} {'k':>4} "
          f"{'Total DET':>10} {'X-det':>7} {'Z-det':>7} {'gongaa':>8} {'excess':>8}")
    print(f"{'-'*20} {'-'*5} {'-'*5} {'-'*5} {'-'*4} "
          f"{'-'*10} {'-'*7} {'-'*7} {'-'*8} {'-'*8}")
    for label, patch, total, xd, zd in [
        ("[[72,12,6]]",  patch_72,  total_72,  x_72,  z_72),
        ("[[144,12,12]]", patch_144, total_144, x_144, z_144),
    ]:
        n_data   = len(patch.data_indices)
        n_x_stab = len(patch.syndrome_indices_x)
        n_z_stab = len(patch.syndrome_indices_z)
        k        = patch.num_logicals
        gongaa   = n_z_stab * (ROUNDS + 1)
        print(f"{label:<20} {n_data:>5} {n_x_stab:>5} {n_z_stab:>5} {k:>4} "
              f"{total:>10} {xd:>7} {zd:>7} {gongaa:>8} {total-gongaa:>8}")

    print(f"\nNotes:")
    print(f"  gongaa formula = n_Z × (rounds+1) counts only Z-type ancilla measurements")
    print(f"  LightStim excess = X-type detectors (LightStim includes both X and Z stabilizer measurements)")
    print(f"\nKey insight: LightStim detectors = Z-type detectors + X-type detectors")
    print(f"  (both X and Z stabilizer measurements are used as detectors for full CSS error detection)")


if __name__ == '__main__':
    main()
